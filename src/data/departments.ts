export const getDepartmentsByScore = (score: number): Department[] => {
  return departments.filter(
    (dept) => score >= dept.score.min && score <= dept.score.max
  );
};

export const getDepartmentsByCity = (city: string): Department[] => {
  return departments.filter((dept) => dept.city.toLowerCase() === city.toLowerCase());
};

export const getDepartmentsByType = (type: 'devlet' | 'vakif'): Department[] => {
  return departments.filter((dept) => dept.type === type);
};

export const getDepartmentsByLanguage = (language: 'turkce' | 'ingilizce'): Department[] => {
  return departments.filter((dept) => dept.language === language);
}; 